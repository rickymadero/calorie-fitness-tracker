export function getExerciseById(id: string) {
  return EXERCISES.find((e) => e.id === id);
}

export function getExercisesByMuscle(muscle: string) {
  return EXERCISES.filter(
    (e) => e.primaryMuscle === muscle || e.secondaryMuscles.includes(muscle as never),
  );
}

export function getReplacementExercises(exerciseId: string) {
  const ex = getExerciseById(exerciseId);
  if (!ex) return [];
  return ex.replacementIds
    .map((id) => getExerciseById(id))
    .filter(Boolean) as ExerciseDefinition[];
}
